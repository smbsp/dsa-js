function xor(arr1) {
  let len = arr1.length;
  let ans = 0;
  for (let i = 1; i <= len; i++) {
    let numOccurance = i * (len - i + 1);
    if (numOccurance % 2 !== 0) {
      ans = ans ^ arr1[i - 1];
    }
  }
  return ans;
}

