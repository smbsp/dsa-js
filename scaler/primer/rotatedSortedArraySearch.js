function rotatedSortedArraySearch(A, B) {
  let low = 0;
  let high = A.length - 1;
  let mid = 0;

  while (low <= high) {
    mid = Math.floor((low + high) / 2);
    if (A[mid] === B) {
      return mid;
    } else if (A[low] < A[mid]) {
      if (B >= A[low] && B <= A[mid]) {
        high = mid - 1;
      } else {
        low = mid + 1;
      }
    } else {
      if (B >= A[mid] && B <= A[high]) {
        low = mid + 1;
      } else {
        high = mid - 1;
      }
    }
  }
  return -1;
}

