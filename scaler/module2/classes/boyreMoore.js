function boyeMoore(A) {
  // Boyerâ€“Moore majority vote algorithm - we modify this a bit. We balance out on three
  // distinct elements
  let item1 = A[0],
    frequency1 = 1;
  let item2 = 0,
    frequency2 = 0;
  let l;
  for (l = 1; l < A.length; l++) {
    if (A[l] == item1) {
      frequency1++;
    } else if (A[l] != item1) {
      item2 = A[l];
      frequency2++;
      break;
    }
  }
  console.log(l, "item1", item1, frequency1, "item2", item2, frequency2);
  console.log("Params1", item1, frequency1, "Params2", item2, frequency2);
  for (let i = l + 1; i < A.length; i++) {
    if (A[i] == item1) {
      frequency1++;
    } else if (A[i] == item2) {
      frequency2++;
    } else if (frequency1 == 0) {
      frequency1++;
      item1 = A[i];
    } else if (frequency2 == 0) {
      frequency2++;
      item2 = A[i];
    } else {
      frequency1--;
      frequency2--;
    }
    // console.log(i, "Params1", item1, frequency1, "Params2", item2, frequency2);
  }
  console.log("Params1", item1, frequency1, "Params2", item2, frequency2);
  let countItem1 = 0;
  let countItem2 = 0;
  for (let j = 0; j < A.length; j++) {
    if (A[j] == item1) {
      countItem1++;
    } else if (A[j] == item2) {
      countItem2++;
    }
  }
  console.log("countItem1", item1, countItem1, "countItem2", item2, countItem2);
  if (countItem1 > Math.floor(A.length / 3)) {
    return item1;
  }
  if (countItem2 > Math.floor(A.length / 3)) {
    return item2;
  }
  return -1;
}

